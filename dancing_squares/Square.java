public class Square
{
  private int width, height;

  private int posX, poxY;

  public Square(int width, int height, int posX, int posY)
  {
    this.width = width;
    this.height = height;

    this.posX = poxX;
    this.posY = posY;
  }

  public int getposX()
  {
    return posX;
  }

  public int getposY()
  {
    return posY;
  }

  public void setposX()
  {
      this.posX = posX;
  }

  public void setPosY()
  {
      this.posY = posY;
  }

}
